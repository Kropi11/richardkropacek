/*Popište, jakým způsobem chápete Dependency Inversion Principle. Uveďte vlastní příklad, na kterém to vysvětlete.

DIP rozumím tak, že program tvoříme nezávisle na hardware, na kterém bude fungovat. Program závisí na abstrakcích, 
nikoli na prostředcích daného hardwaru. To znamená, že jeden program můžeme implementovat na různý hardware, 
aniž bychom zásadně měnili dosavadní kód (Vytvoříme program, která bude zaznamenávat klávesnici počítače. 
Zároveň ho ale můžeme doplnit o kód, který ho bude implementovat tak, aby zaznamenával stisknutí tlačítka připojeného na Arduino. 
Základní kód bude ale přitom zachován.).*/
